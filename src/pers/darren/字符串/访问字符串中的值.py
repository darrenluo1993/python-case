#!/usr/bin/python3
# 索引值以 0 为开始值，-1 为从末尾的开始位置。

var1 = 'Hello World!'
var2 = "Runoob"

print("var1[0]: ", var1[0])
print("var2[1:5]: ", var2[1:5])
print("var1[1:5]: ", var1[1:5])
print("var1[:5]: ", var1[:5])
print("var1[1:]: ", var1[1:])
print("var1[-2:]: ", var1[-2:])
print("var1[-2:-1]: ", var1[-2:-1])
print("var1[:-2]: ", var1[:-2])
print("var1[0:-2]: ", var1[0:-2])
